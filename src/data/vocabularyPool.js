// Test structure configuration for random selection
export const STANDARD_TEST_STRUCTURE = [
  { level: 'A2', count: 2 },
  { level: 'B1', count: 3 },
  { level: 'B2', count: 3 },
  { level: 'C1', count: 2 }
];

// Helper function to generate a random test from the pool
export const generateRandomTest = () => {
  const testQuestions = [];
  
  // Process levels in order: A2 â†’ B1 â†’ B2 â†’ C1
  STANDARD_TEST_STRUCTURE.forEach(({ level, count }) => {
    const levelPool = VOCABULARY_POOL[level];
    if (!levelPool || levelPool.length === 0) {
      console.warn(`No questions available for level ${level}`);
      return;
    }
    
    // Create a shuffled copy of the level pool to get random questions from this level
    const shuffled = [...levelPool].sort(() => Math.random() - 0.5);
    
    // Take the required number of questions
    const selected = shuffled.slice(0, Math.min(count, shuffled.length));
    
    // Add level information to each question
    const questionsWithLevel = selected.map(question => ({
      ...question,
      level: level
    }));
    
    // Add questions to test in order (don't shuffle at the end)
    testQuestions.push(...questionsWithLevel);
  });
  
  // Return questions in progressive difficulty order (no final shuffle)
  console.log('ðŸ“š Generated test with progressive difficulty:', testQuestions.map((q, index) => ({
    position: index + 1,
    level: q.level,
    word: q.answer
  })));
  
  return testQuestions;
};

